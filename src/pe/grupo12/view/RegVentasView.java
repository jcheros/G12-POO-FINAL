    // Evento de selección de tipos de publicación
    public void cbxTipoPublicacionItemStateChanged(ItemEvent e) {
        if (e.getStateChange() == ItemEvent.SELECTED) {
            setCbxPublicacionItems(e.getItem().toString());
        }
    }
    
    // Evento de selección de publicación
    public void cbxPublicacionItemStateChanged(ItemEvent e) {
        if (e.getStateChange() == ItemEvent.SELECTED) {
            setStockValue();
        }
    }
    
    private void setCbxPublicacionItems(String tipo) {
        cbxPublicacion.removeAllItems();
        publicaciones = publicacionService.listarPorTipo(tipo);
        publicaciones.forEach(item -> cbxPublicacion.addItem(String.format("%s - %s", item.getId(), item.getTitulo())));
        setStockValue();
    }
    
    private void setStockValue() {
        String[] valores = cbxPublicacion.getSelectedItem().toString().split("-");
        Optional<Publicacion> seleccionOptional = publicaciones.stream().filter(item -> item.getId().equals(valores[0].trim())).findAny();
            
        if (seleccionOptional.isPresent()) {
            publicacionSeleccionada = seleccionOptional.get();
            txtStock.setText(String.valueOf(publicacionSeleccionada.getStock()));
            txtPrecio.setText(String.valueOf(publicacionSeleccionada.getPrecio()));
            calcularMontos();
        }
    }

