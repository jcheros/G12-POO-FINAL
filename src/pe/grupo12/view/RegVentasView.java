    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:        
        venta.setIdEmpleado(Integer.parseInt(txtIdEmpleado.getText()));
        venta.setCliente(txtCliente.getText());
        
        try {
            venta.setFecha(format.parse(txtFechaVenta.getText()));
        } catch (ParseException e) {
            venta.setFecha(new Date());
        }
        
        venta.setIdPublicacion(publicacionSeleccionada.getId());
        
        Venta ventaBD = ventaService.registrarVenta(venta);
        limpiarFormulario();
        
        model.addRow(new Object[]{
            ventaBD.getId(),
            ventaBD.getCliente(),
            format.format(ventaBD.getFecha()),
            ventaBD.getIdEmpleado(),
            ventaBD.getIdPublicacion(),
            ventaBD.getCantidad(),
            ventaBD.getPrecio(),
            ventaBD.getDcto(),
            ventaBD.getSubTotal(),
            ventaBD.getImpuesto(),
            ventaBD.getTotal()
        });
    }//GEN-LAST:event_btnGuardarActionPerformed

